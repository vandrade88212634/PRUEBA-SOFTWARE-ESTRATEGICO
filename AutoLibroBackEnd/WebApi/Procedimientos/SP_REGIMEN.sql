
IF EXISTS (select * from dbo.sysobjects where id = object_id(N'[dbo].[SP_REGIMEN]') and xtype in (N'FN', N'IF', N'TF', N'P'))
drop procedure [dbo].[SP_REGIMEN] 
GO 
SET ANSI_NULLS ON 
GO 
SET QUOTED_IDENTIFIER ON 
GO 

CREATE PROCEDURE dbo.SP_REGIMEN ( 
  @OPCION_PROCESO int = NULL, 
  @ID INT = NULL , 
  @DESCRIPCION NVARCHAR(100) = NULL 
) 

AS 
BEGIN 
IF @OPCION_PROCESO = 1
BEGIN
select 
ID , 
DESCRIPCION 
from Gen.REGIMEN
WHERE (@ID = 0 OR (@ID <> 0 AND ID = @ID)) 
END

IF @OPCION_PROCESO = 2
BEGIN
INSERT INTO Gen.REGIMEN ( 
  DESCRIPCION 
) VALUES ( 
  @DESCRIPCION 
)
--SELECT SCOPE_IDENTITY() 'ID' 
declare @Key int = @@identity
select * from Gen.REGIMEN where ID = @Key
END
IF @OPCION_PROCESO = 3
BEGIN
UPDATE Gen.REGIMEN SET 
DESCRIPCION = @DESCRIPCION 
WHERE
ID = @ID 
select * from Gen.REGIMEN where ID = @ID
END

IF @OPCION_PROCESO = 4
BEGIN
DELETE FROM Gen.REGIMEN
WHERE 
ID = @ID 
END
IF @OPCION_PROCESO = 5
BEGIN
select 'buenas'
END




END 
GO 


